package javaconcepts;

public class RegularExpression {

	/*
	 * common concepts
	 * 
	 * '/s' means one more whitespaces
	 * ^\d+(\.\d+)?
	 * 
	 * ^ means pattern must start beginingof line
	 * \d+ one or more digits
	 * ? makes expression in bracket optional
	 * \. for grouping
	 * 
	 * . matches any character
	 * $ must match end line
	 * [abc] can match any letter
	 *  [abc][nm] can match any abc followed by any m or n
	 *  [^abc] negates the pattern
	 *  [1-7a-z] range matching
	 *  \d any digit
	 *  \D any non digit
	 *  \s a whitespace
	 *  \S a non whitespace
	 *  \w a word
	 *  \W a non word
	 *  * occurs zero or more times (0,)
	 *  + one or several chracter (1,0)
	 *  ? (0,1)
	 *  {X} x number of times
	 *  {X,Y} between x and y
	 *  s.matches("regex") if regex matches s
	 *  s.matches("regex") split by regex
	 *  
	 *  
	 * 
	 * 
	 */
	
}
